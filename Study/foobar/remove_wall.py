# [3,0  3,1  3,2  3,3  3,4]
class Node:
    def __init__(self, row, col, walls_penetrable) -> None:
        self.row = row
        self.col = col
        self.walls_penetrable = walls_penetrable
    
def solution(map):
    node = Node(row=0,col=0, walls_penetrable=1)
    distance = [[-1 for _ in range(len(map[0]))] for _ in range(len(map))]
    distance[0][0] = 1
    queue = [node]
    while queue:
        node = queue.pop()
        if node.row == (len(map) - 1) and node.col == (len(map[0]) - 1):
            return distance[node.row][node.col]

        neighbors = get_neighbors(node=node, map=map)
        if neighbors:
            for neighbor in neighbors:
                if distance[neighbor.row][neighbor.col] == -1:
                    distance[neighbor.row][neighbor.col] = distance[node.row][node.col] + 1
                    queue.append(neighbor)

def get_neighbors(node, map):
    """Get valid neighbors for a node"""
    neighbors = []
    right = (node.row, node.col + 1)
    left = (node.row, node.col - 1)
    top = (node.row - 1, node.col)
    bottom = (node.row + 1, node.col)

    if right[1] < len(map[0]):
        if map[right[0]][right[1]] == 1:
            if node.walls_penetrable > 0:
                neighbors.append(Node(right[0], right[1], node.walls_penetrable - 1))
        else:
            neighbors.append(Node(right[0], right[1], node.walls_penetrable))
    
    if left[1] >= 0:
        if map[left[0]][left[1]] == 1:
            if node.walls_penetrable > 0:
                neighbors.append(Node(left[0], left[1], node.walls_penetrable - 1))
        else:
            neighbors.append(Node(left[0], left[1], node.walls_penetrable))
    
    if top[0] >= 0:
        if map[top[0]][top[1]] == 1:
            if node.walls_penetrable > 0:
                neighbors.append(Node(top[0], top[1], node.walls_penetrable - 1))
        else:
            neighbors.append(Node(top[0], top[1], node.walls_penetrable))
        
    if bottom[0] < len(map):
        if map[bottom[0]][bottom[1]] == 1:
            if node.walls_penetrable > 0:
                neighbors.append(Node(bottom[0], bottom[1], node.walls_penetrable - 1))
        else:
            neighbors.append(Node(bottom[0], bottom[1], node.walls_penetrable))
    return neighbors

